// ========================================
// Поверхносное копирование обьекта, не копирует вложенные обьекты, а только те которые лежат на первом уровне
// Способ с цыклами
// ========================================

// Функция создающая клон
function copy(mainObj){
  let objCopy = {};

  let key;
  for(key in mainObj){
    objCopy[key] = mainObj[key];
  }

  return objCopy;
}

// обьект который нужно склонировать
let numbers = {
  a: 5,
  b: 7,
  c: {
    x: 'seven',
    y: 4
  }
};

// Клон
const newNumbers = copy(numbers);

// ========================================
// Поверхносное копирование обьекта, соединяем несколько обьектов
// Object.assign()
// ========================================

const oneObj = {
  a: 5,
  b: 7,
  c: {
    x: 'seven',
    y: 4
  }
};

const twoObj = {
  d: 17,
  e: 20
};

// Слияние двух обьектов и создание поверхносной копии

let clone = Object.assign(oneObj, twoObj); // { a: 5, b: 7, c: { x: 'seven', y: 4 }, d: 17, e: 20 }

//========== Просто создание поверхносной копии

let onlyClone = Object.assign({}, twoObj); // { d: 17, e: 20 }


