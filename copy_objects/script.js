// ========================================
// Создание поверхносной копии, новые стандарты ES6-ES9
// операторы разворота(спред оператор)
// ========================================

const video = ['youtube', 'vimeo', 'rutube'],
      blogs = ['wordpress', 'livejournal', 'blogger'];

// копируем 2 массива в один
const internet = [...video, ...blogs];

// копируем 2 массива в один + свои данные
const internetTwo = [...video, ...blogs, 'vk', 'fackebook'];

// ========================================
// Передаем в аргументы функции массив
// операторы разворота(спред оператор)
// ========================================

function log(a, b, c){
  console.log(a);
  console.log(b);
  console.log(c);
}

const num = [2, 7, 8];

log(...num); // 2  7  8

// ========================================
// Создание поверхносных копий МАССИВОВ, четвертый способ
// операторы разворота(спред оператор)
// ========================================

const array = ['a', 'b'];

const arrayNew = [...array]; // [ 'a', 'b' ]

// ========================================
// Создание поверхносных копий ОБЬЕКТОВ, четвертый способ
